        <article class="md:w-2/3 md:pr-4 space-y-4">
            <p>
                Förskolans verksamhet baseras på ”Läroplan för förskolan”
                <x-link
                    href="https://www.skolverket.se/download/18.6bfaca41169863e6a65d5aa/1553968116077/pdf4001.pdf"
                    target="_blank"
                >Lpfö 98</x-link>, reviderad 2018.
            </p>
            <p>
                Vårt arbetssätt är inspirerat av förhållningssättet i Reggio Emilia
                – ett pedagogiskt arbetssätt som bygger på en tro på människans
                möjligheter, en djup respekt för barnet samt en övertygelse om
                att alla barn föds rika och intelligenta med en stark inneboende
                drivkraft att utforska världen.
            </p>
            <p class="italic">
                – vi ger dem möjlighet och stimulans till att utveckla sin egen
                förmåga, kunskap och förståelse i samspel med omgivningen.
            </p>
            <p class="italic">
                – vi involverar alla sinnen och använder många språk
                (bild, tal, skrift och kroppsspråk, rörelse, musik och matematik etc.)
            </p>
            <p class="italic">
                – vi låter fantasi och humor spela en stor roll
            </p>
            <p class="italic">
                – barnen får prova sina egna funderingar och teorier, på så sätt
                ger vi barnen en tilltro till den egna förmågan att skapa, lära,
                välja och påverka
            </p>
            <p class="italic">
                – vi ser barngruppen som en pedagogisk resurs, som tillsammans
                prövar hypoteser och söker lösningar
            </p>
            <p class="italic">
                – vårt instrument för att lyssna och ta till vara barnens tankar
                och idéer är pedagogisk dokumentation
            </p>
            <p class="italic">
                – vi synliggör barnens utveckling för barnet självt, föräldrarna
                samt oss själva via enskilda portfoliopärmar
            </p>
            <p>
                På förskolan finns det dels övergripande mål för barngruppen och
                verksamheten dels enskilda mål för varje barn.
            </p>
